 $(function(){
 	//隐藏按钮
 	if($("#note").length>0){
 		$("#btn").hide();
 	}
 	//提交选课
 	$(document).on('click','#sub',function(){
 		$.ajax({
 			url:'/selectcourse/select',
 			type:'POST',
 			data:$('form').serialize(),
 			success:function(msg){
 				if(msg){
 					setTimeout("window.location.reload()",2000);
 					box('ok',"选课成功");
 				}else{
 					box('error',"选课失败");
 				}
 			},
 			error:function(){
 				box('error',"网络错误");
 			}
 		})
 	})
 })